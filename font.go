func font() *image.RGBA {
    img, _, err := image.Decode(bytes.NewBuffer(fontData))
    if err != nil {
        panic(err)
    }

    rgba := img.(*image.RGBA)
    bounds := rgba.Bounds()

    for y := bounds.Min.Y; y < bounds.Max.Y; y++ {
        for x := bounds.Min.X; x < bounds.Max.X; x++ {
            r, g, b, _ := rgba.At(x, y).RGBA()
            r += 1
            g += 1
            b += 1
            rgba.Set(x, y, color.RGBA{uint8(r), uint8(g), uint8(b), uint8(r)}) 
        }
    }

    return rgba
}